 <h1>CUZCO</h1>
 <p>El Cuzco o Cusco5 (en quechua sureño: Qusqu o Qosqo, pronunciado [ˈqo̝s.qɔ]) es una ciudad del sureste del Perú ubicada en la vertiente oriental de la cordillera de los Andes, en la cuenca del río Huatanay, afluente del Vilcanota. Es la capital del departamento del Cuzco y, además, según está declarado en la constitución peruana, es la «capital histórica» del país.

La ciudad del Cuzco, según el Instituto Nacional de Estadística e Informática, es la octava ciudad más poblada del Perú, y albergaba, en el 2014, una población de 420, 137 habitantes.6

Antiguamente fue la capital del Imperio inca y una de las ciudades más importantes del Virreinato del Perú, en cuya época, y en manos de los españoles, se engalanó de iglesias, palacios y plazas barrocas y neoclásicas, que es lo que hoy le convierten en el principal lugar turístico de Perú. Declarada Patrimonio de la Humanidad en 1983 por la Unesco, suele ser denominada, debido a la gran cantidad de monumentos que posee, la "Roma de América".</p>
